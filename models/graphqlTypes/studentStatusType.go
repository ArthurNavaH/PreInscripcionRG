package graphqlTypes

import "github.com/graphql-go/graphql"

//StudentStatusType Objeto GraphQL para estados de estudiantes.
var StudentStatusType = graphql.NewObject(
	graphql.ObjectConfig{
		Name: "Sector",
		Fields: graphql.Fields{
			"id": &graphql.Field{
				Type: graphql.Int,
			},
			"userID": &graphql.Field{
				Type: graphql.Int,
			},
			"name": &graphql.Field{
				Type: graphql.String,
			},
			"description": &graphql.Field{
				Type: graphql.String,
			},
			"message": &graphql.Field{
				Type: graphql.String,
			},
			"code": &graphql.Field{
				Type: graphql.Int,
			},
		},
	},
)
